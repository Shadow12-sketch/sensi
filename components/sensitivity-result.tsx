"use client";

import { useState } from "react";
import { Button } from "@/components/ui/button";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import { Copy, Check, Save, Crosshair, Sparkles, Info } from "lucide-react";
import type { SensitivityResult as SensResult } from "@/lib/sensitivity-calculator";
import type { Platform, Playstyle, PingLevel } from "@/lib/sensitivity-calculator";
import ReactMarkdown from "react-markdown";

interface SensitivityResultProps {
  result: SensResult;
  explanation: string;
  deviceInfo: {
    deviceName: string;
    platform: Platform;
    playstyle: Playstyle;
    pingLevel: PingLevel;
    dpi: number | null;
  };
  onSavePreset: (name: string) => void;
}

const sensitivityLabels: Record<keyof SensResult, string> = {
  general: "General",
  redDot: "Red Dot",
  scope2x: "2x Scope",
  scope4x: "4x Scope",
  awmScope: "AWM Scope",
  freeLook: "Free Look",
};

const sensitivityConfig: Record<keyof SensResult, { gradient: string; icon: string }> = {
  general: { gradient: "from-cyan-400 to-cyan-600", icon: "bg-cyan-500" },
  redDot: { gradient: "from-red-400 to-red-600", icon: "bg-red-500" },
  scope2x: { gradient: "from-blue-400 to-blue-600", icon: "bg-blue-500" },
  scope4x: { gradient: "from-violet-400 to-violet-600", icon: "bg-violet-500" },
  awmScope: { gradient: "from-amber-400 to-amber-600", icon: "bg-amber-500" },
  freeLook: { gradient: "from-emerald-400 to-emerald-600", icon: "bg-emerald-500" },
};

export function SensitivityResultComponent({
  result,
  explanation,
  deviceInfo,
  onSavePreset,
}: SensitivityResultProps) {
  const [copied, setCopied] = useState(false);
  const [presetName, setPresetName] = useState("");
  const [showSaveInput, setShowSaveInput] = useState(false);
  const [saved, setSaved] = useState(false);

  const handleCopy = () => {
    const text = `Free Fire Sensitivity Settings
Device: ${deviceInfo.deviceName}
Platform: ${deviceInfo.platform.toUpperCase()}
Playstyle: ${deviceInfo.playstyle}
Ping: ${deviceInfo.pingLevel}

General: ${result.general}
Red Dot: ${result.redDot}
2x Scope: ${result.scope2x}
4x Scope: ${result.scope4x}
AWM Scope: ${result.awmScope}
Free Look: ${result.freeLook}

Generated by FF Sensitivity Generator`;

    navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleSave = () => {
    if (presetName.trim()) {
      onSavePreset(presetName.trim());
      setPresetName("");
      setShowSaveInput(false);
      setSaved(true);
      setTimeout(() => setSaved(false), 2000);
    }
  };

  const playstyleDisplay: Record<Playstyle, string> = {
    freestyle: "Freestyle",
    instaplayer: "Instaplayer",
    rusher: "Rusher",
    balanced: "Balanced",
    onetap: "One-Tap",
    sniper: "Sniper",
  };

  const pingDisplay: Record<PingLevel, { label: string; color: string }> = {
    low: { label: "Low", color: "text-green-400" },
    medium: { label: "Medium", color: "text-yellow-400" },
    high: { label: "High", color: "text-red-400" },
  };

  return (
    <div className="space-y-6">
      {/* Main Result Card */}
      <Card className="gaming-card border-primary/30 bg-card/60 backdrop-blur-xl overflow-hidden">
        <CardHeader className="border-b border-border/30 bg-gradient-to-r from-primary/10 via-primary/5 to-transparent">
          <CardTitle className="flex items-center gap-3 text-xl">
            <div className="p-2 rounded-lg bg-primary/10 border border-primary/30 animate-pulse-glow">
              <Crosshair className="h-5 w-5 text-primary" />
            </div>
            Your Sensitivity
          </CardTitle>
        </CardHeader>
        <CardContent className="p-6">
          {/* Device Info Summary */}
          <div className="mb-6 grid grid-cols-2 md:grid-cols-4 gap-4 p-4 rounded-xl bg-card/50 border border-border/30">
            <div className="space-y-1">
              <span className="text-muted-foreground text-xs uppercase tracking-wider block">Device</span>
              <span className="font-medium text-sm truncate block">{deviceInfo.deviceName}</span>
            </div>
            <div className="space-y-1">
              <span className="text-muted-foreground text-xs uppercase tracking-wider block">Platform</span>
              <span className="font-medium text-sm capitalize">{deviceInfo.platform}</span>
            </div>
            <div className="space-y-1">
              <span className="text-muted-foreground text-xs uppercase tracking-wider block">Playstyle</span>
              <span className="font-medium text-sm text-primary">{playstyleDisplay[deviceInfo.playstyle]}</span>
            </div>
            <div className="space-y-1">
              <span className="text-muted-foreground text-xs uppercase tracking-wider block">Ping</span>
              <span className={`font-medium text-sm ${pingDisplay[deviceInfo.pingLevel].color}`}>
                {pingDisplay[deviceInfo.pingLevel].label}
              </span>
            </div>
          </div>

          {/* Sensitivity Values Grid */}
          <div className="grid grid-cols-2 md:grid-cols-3 gap-3">
            {(Object.keys(result) as (keyof SensResult)[]).map((key, index) => (
              <div
                key={key}
                className="group relative overflow-hidden rounded-xl border border-border/30 bg-card/50 p-4 hover:border-border/60 transition-all duration-300 hover:scale-[1.02]"
                style={{ animationDelay: `${index * 100}ms` }}
              >
                {/* Background gradient */}
                <div
                  className={`absolute inset-0 bg-gradient-to-br ${sensitivityConfig[key].gradient} opacity-0 group-hover:opacity-5 transition-opacity duration-300`}
                />
                
                <div className="relative">
                  <div className="flex items-center gap-2 mb-2">
                    <span className={`w-2 h-2 rounded-full ${sensitivityConfig[key].icon}`} />
                    <span className="text-xs text-muted-foreground font-medium uppercase tracking-wider">
                      {sensitivityLabels[key]}
                    </span>
                  </div>
                  <span className="text-3xl font-bold text-foreground tabular-nums">
                    {result[key]}
                  </span>
                  {/* Progress bar */}
                  <div className="mt-3 h-1 rounded-full bg-border/30 overflow-hidden">
                    <div
                      className={`h-full rounded-full bg-gradient-to-r ${sensitivityConfig[key].gradient} transition-all duration-700 ease-out`}
                      style={{ 
                        width: `${(result[key] / 200) * 100}%`,
                      }}
                    />
                  </div>
                </div>
              </div>
            ))}
          </div>

          {/* Action Buttons */}
          <div className="mt-6 flex flex-wrap gap-3">
            <Button
              variant="secondary"
              onClick={handleCopy}
              className="flex-1 sm:flex-none bg-secondary/80 hover:bg-secondary border-border/50 transition-all duration-300 hover:scale-[1.02]"
            >
              {copied ? (
                <>
                  <Check className="mr-2 h-4 w-4 text-primary" />
                  Copied!
                </>
              ) : (
                <>
                  <Copy className="mr-2 h-4 w-4" />
                  Copy All
                </>
              )}
            </Button>
            
            {!showSaveInput ? (
              <Button
                variant="outline"
                onClick={() => setShowSaveInput(true)}
                className="flex-1 sm:flex-none border-border/50 bg-transparent hover:bg-card/50 transition-all duration-300 hover:scale-[1.02]"
              >
                {saved ? (
                  <>
                    <Check className="mr-2 h-4 w-4 text-primary" />
                    Saved!
                  </>
                ) : (
                  <>
                    <Save className="mr-2 h-4 w-4" />
                    Save Preset
                  </>
                )}
              </Button>
            ) : (
              <div className="flex gap-2 flex-1 sm:flex-none animate-in fade-in slide-in-from-left-2 duration-300">
                <Input
                  placeholder="Preset name..."
                  value={presetName}
                  onChange={(e) => setPresetName(e.target.value)}
                  className="bg-input/50 border-border/50 w-40"
                  onKeyDown={(e) => e.key === "Enter" && handleSave()}
                  autoFocus
                />
                <Button onClick={handleSave} size="sm" className="bg-primary text-primary-foreground">
                  Save
                </Button>
                <Button
                  variant="ghost"
                  size="sm"
                  onClick={() => setShowSaveInput(false)}
                  className="text-muted-foreground hover:text-foreground"
                >
                  Cancel
                </Button>
              </div>
            )}
          </div>
        </CardContent>
      </Card>

      {/* Explanation Card */}
      <Card className="gaming-card border-border/50 bg-card/60 backdrop-blur-xl overflow-hidden">
        <CardHeader className="border-b border-border/30">
          <CardTitle className="text-lg flex items-center gap-2">
            <Info className="h-4 w-4 text-primary" />
            Why These Settings?
          </CardTitle>
        </CardHeader>
        <CardContent className="p-6 prose prose-invert prose-sm max-w-none">
          <ReactMarkdown
            components={{
              strong: ({ children }) => (
                <strong className="text-primary font-semibold">{children}</strong>
              ),
              p: ({ children }) => (
                <p className="text-muted-foreground leading-relaxed mb-4 last:mb-0">{children}</p>
              ),
              hr: () => <hr className="border-border/30 my-4" />,
              ul: ({ children }) => (
                <ul className="text-muted-foreground space-y-2 list-none pl-0 mb-4">{children}</ul>
              ),
              li: ({ children }) => (
                <li className="text-foreground flex items-start gap-2">
                  <Sparkles className="h-4 w-4 text-primary shrink-0 mt-0.5" />
                  <span>{children}</span>
                </li>
              ),
            }}
          >
            {explanation}
          </ReactMarkdown>
        </CardContent>
      </Card>
    </div>
  );
}
